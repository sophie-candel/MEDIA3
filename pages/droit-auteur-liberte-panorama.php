                    <h3>Les principes de la liberté de panorama</h3>
                    <p>Un particulier peut, en toute légalité, prendre une photographie d’un monument ou d’une sculpture situé sur la voie publique. Il peut également la diffuser sur les réseaux sociaux, sans pour autant entraîner des poursuites judiciaires. Et cela grâce à la liberté de panorama ! Elle apparaît comme une <a href="droit-image-biens.php?#droit-auteur-exceptions">exception au droit d’auteur</a>. Seules conditions à cette liberté : les oeuvres doivent être situées en PERMANENCE dans un lieu public. Les expositions temporaires ne sont donc nullement affectée par cette exception. De plus, les représentations et reproductions doivent être à but non lucratif, c’est-à-dire, à but non commercial.</p>


                    <h3>Le droit d’auteur</h3>
                    <p>Pour rappel, le droit d’auteur appartient à la propriété intellectuelle et protège les oeuvres de l’esprit (en d’autres termes, les oeuvres matérialisées et originales.)


                    Il se divise en deux branches : les droits moraux (perpétuels, inaliénables, imprescriptibles) et les droits patrimoniaux (durée de 70 ans qui suit le 1er janvier après la mort de l’auteur, cessibles et prescriptibles. Dans les droits moraux on retrouve, les droits de divulgation, paternité, au respect de l’oeuvre, de retrait et de repentir. Quant aux droits patrimoniaux, on retrouve les droits de reproduction, de représentation, de suite pour les oeuvres graphiques ou plastiques. 


                    Les droits moraux étant inaliénables, seuls les droits patrimoniaux sont concernés par la liberté de panorama ; plus précisément le droit de reproduction (fait de faire des copies) et le droit de représentation (fait de montrer l’oeuvre à un public).</p>


                    <h3>Une loi récente</h3>
                    <img alt=”Louvres Paris”> (pixabay)
                    <p>La liberté de panorama est assez récente. En effet, elle est datée de janvier 2016, et s’intègre au projet de loi pour une République numérique. Ce qui signifie qu’avant qu’elle soit adoptée, toute personne ayant diffusé l’image d’une oeuvre architecturale telle que la pyramide du Louvres aurait pu être traînée en justice.</p>


                    <h3>Un combat pour préserver les droits d’auteurs</h3>
                    <p> Suite à cet amendement, les auteurs et diverses organisations telles que la société des Auteurs Dans les Arts Graphiques et Plastiques se sont battues pour que la loi ne touche que les particuliers. 


                    Cette organisation a par ailleurs affirmé que “les artistes et leurs représentants n'ont jamais assigné en justice et encore moins fait condamner un particulier pour avoir fait circuler ses photos. Les particuliers ont toujours pu partager librement des photos sur les réseaux sociaux !” (extrait d’un communiqué de la ADAGP qui répond à Wikimédia, parti pour la liberté de panorama)</p>


                    <h3>Les réseaux sociaux</h3>
                    Une question subsiste malgré tout : peut-on clairement définir que les particuliers ont le droit de diffuser leurs photos de monuments grâces à la liberté de panorama sur les réseaux sociaux ? Il ne faut pas oublier que ces mêmes réseaux sociaux sont des entreprises et que leur but reste avant tout d’engendrer des bénéfices. Un paradoxe s’opère. 

